    public void GetData(){
        ArrayList<OrderModel> orderList = GetOrdes();
        grdOrder.setModel(modelo);
        
        modelo.addColumn("idComanda");
        modelo.addColumn("Hora");
        modelo.addColumn("Fecha");
        modelo.addColumn("Cliente");
        modelo.addColumn("Atendio por");
        modelo.addColumn("Estatus");
        modelo.addColumn("Valor");
        
        for(OrderModel ord: orderList)
            {
                 Object[] filas2 = new Object[8];//9
                 filas2[0] = ord.idComanda;
                 filas2[1] = ord.Hora;
                 filas2[2] = ord.Fecha;
                 filas2[3] = ord.NombreCli;
                 filas2[4] = ord.NombreEmp;
                 filas2[5] = ord.Estatus;
                 filas2[6] = ord.Valor;
                 
                 modelo.addRow(filas2);
            }
    }
    
