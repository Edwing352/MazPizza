    private void btnDeleteEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteEmpActionPerformed
        //Eliminaci贸n de datos
        int idEmployee = Integer.parseInt(txtIdEmpleado.getText());

        boolean corret = DeleteEmployees(idEmployee);

        if(corret)
        {
            JOptionPane.showMessageDialog(null, "Se elimino el IdEmpleado " +idEmployee+ "correctamente.");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "El empleado no existe, intentelo de nuevo.");
        }
    }//GEN-LAST:event_btnDeleteEmpActionPerformed

    private void btnUpdateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmpActionPerformed
        //Edici贸n de datos
        int idEmployee = 0;

        if(!StringUtils.isNullOrEmpty(txtIdEmpleado.getText()))
        {
            idEmployee = Integer.parseInt(txtIdEmpleado.getText());

            EmployeeModel employee = existsEmployees(idEmployee);

            if(employee.idEmpleado == idEmployee){
                new CreateEmployeeView(employee).setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(null, "El empleado que intentas editar no existe, intentelo de nuevo.");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Es necesario ingresar un idEmpleado para editar.");
        }
    }//GEN-LAST:event_btnUpdateEmpActionPerformed

