    public void GetData()
    {
        ArrayList<EmployeeModel> employeeList = GetEmployees();
        //aqui vas a limpiar el grid, antes de cargarle el modelo
        grdEmployee.setModel(modelo);
        
        modelo.addColumn("Id");
        modelo.addColumn("Nombre");
        modelo.addColumn("Direcci贸n");
        modelo.addColumn("Numero SS");
        modelo.addColumn("Colonia");
        //modelo.addColumn("Fecha Contrato");
        modelo.addColumn("Nombre Usuario");
        modelo.addColumn("Password");
        modelo.addColumn("Puesto");
        
        for(EmployeeModel emp: employeeList)
            {
                 Object[] filas2 = new Object[8];//9
                 filas2[0] = emp.idEmpleado;
                 filas2[1] = emp.NombreEmp;
                 filas2[2] = emp.CalleNumero;
                 filas2[3] = emp.NumeroSS;
                 filas2[4] = emp.Colonia;
                 //filas2[4] = emp.FechaContrato;
                 filas2[5] = emp.NombreUsuario;
                 filas2[6] = emp.Password;
                 filas2[7] = emp.NombreP;
                 
                 modelo.addRow(filas2);
            }
    }
    

