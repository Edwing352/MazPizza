    private void BtnAddProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddProActionPerformed
        ProductsModel newPro = new ProductsModel();
        String NombreProducto = txtNameProduct.getText();
        String Descripcion = txtDesProduct.getText();
        String PrecioProducto = txtPreProduct.getText();
        
        if((!NombreProducto.isBlank() || !NombreProducto.isEmpty()) &&
           (!Descripcion.isBlank() || !Descripcion.isEmpty()) &&
           (!PrecioProducto.isBlank() || !PrecioProducto.isEmpty())){
            
                newPro.NombreProducto = txtNameProduct.getText();
                newPro.Descripcion = txtDesProduct.getText();
                newPro.PrecioProducto =  Double.parseDouble(txtPreProduct.getText());        

                if(editing)
                {
                    newPro.idProductos = Integer.parseInt(txtIdProducts.getText());
                }

                boolean correct = InsertProduct(newPro, editing);

                if(correct)
                {
                    JOptionPane.showMessageDialog(null, "El producto se guardo correctamente.");
                    super.dispose();
                }
        }
        else{
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios.");
        }
    }//GEN-LAST:event_BtnAddProActionPerformed

